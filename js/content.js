function fillLessonWords(words, themeName) {
  const result = [...words].slice(0, WORDS_PER_THEME);
  const lessonSeed = Math.max(0, THEME_NAMES.indexOf(themeName));
  let counter = 1;
  while (result.length < WORDS_PER_THEME) {
    const fallbackWord = COMMON_WORD_POOL[(lessonSeed * 11 + counter) % COMMON_WORD_POOL.length];
    result.push({ en: fallbackWord.en, ua: fallbackWord.ua });
    counter += 1;
  }
  return result;
}

function fillLessonPhrases(phrases, themeName, phrasesPerTheme) {
  const result = [...phrases].slice(0, phrasesPerTheme);
  const lessonSeed = Math.max(0, THEME_NAMES.indexOf(themeName));
  let counter = 1;
  while (result.length < phrasesPerTheme) {
    const fallbackPhrase = COMMON_PHRASE_POOL[(lessonSeed * 13 + counter) % COMMON_PHRASE_POOL.length];
    result.push({ en: fallbackPhrase.en, ua: fallbackPhrase.ua });
    counter += 1;
  }
  return result;
}

