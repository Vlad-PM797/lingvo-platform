function createProjectIntros() {
  const intros = {};
  THEME_NAMES.forEach((themeName, themeIndex) => {
    const phrasesPerTheme = BASE_PHRASES_PER_THEME + (themeIndex < EXTRA_PHRASES_THEMES ? 1 : 0);
    const override = LESSON_OVERRIDES[themeName];
    if (override) {
      intros[themeName] = {
        words: fillLessonWords(override.words, themeName),
        phrases: fillLessonPhrases(override.phrases, themeName, phrasesPerTheme),
        themeOrder: themeIndex + 1,
      };
      return;
    }
    intros[themeName] = {
      words: fillLessonWords([], themeName),
      phrases: fillLessonPhrases([], themeName, phrasesPerTheme),
      themeOrder: themeIndex + 1,
    };
  });
  return intros;
}

